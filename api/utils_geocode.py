# デフォルトの緯度経度（東京）
DEFAULT_LAT = 35.6762
DEFAULT_LON = 139.6503


def geocode(place_name: str) -> tuple[float, float]:
    """地名から緯度経度を取得する

    Args:
        place_name: 出生地の文字列（例: "札幌市", "東京"）

    Returns:
        (緯度, 経度) のタプル。取得できない場合は東京のデフォルト値を返す。
    """
    if not place_name or not place_name.strip():
        return DEFAULT_LAT, DEFAULT_LON

    place = place_name.strip()

    # 1. ハードコード辞書から検索（部分一致も試行）
    if place in JAPAN_CITIES:
        return JAPAN_CITIES[place]

    # 部分一致: 「東京都港区」→「東京都」「東京」で検索
    for city_name, coords in JAPAN_CITIES.items():
        if city_name in place or place in city_name:
            return coords

    # 2. Nominatim APIにフォールバック
    try:
        resp = requests.get(
            "https://nominatim.openstreetmap.org/search",
            params={
                "q": place,
                "format": "json",
                "limit": 1,
                "countrycodes": "jp",
            },
            headers={"User-Agent": "astral-horoscope/1.0"},
            timeout=5,
        )
        if resp.status_code == 200:
            results = resp.json()
            if results:
                lat = float(results[0]["lat"])
                lon = float(results[0]["lon"])
                logger.info(f"ジオコーディング成功: {place} → ({lat}, {lon})")
                return lat, lon
    except Exception as e:
        logger.warning(f"Nominatim APIエラー: {e}")

    # 3. 取得できない場合はデフォルト値（東京）
    logger.info(f"ジオコーディング失敗、デフォルト値を使用: {place}")
    return DEFAULT_LAT, DEFAULT_LON
